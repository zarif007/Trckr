#!/usr/bin/env node
import fs from 'node:fs'
import path from 'node:path'

const ROOT = process.cwd()
const TARGET = path.join(ROOT, 'docs', '_generated', 'file-map.md')
const INCLUDE_ROOTS = ['app', 'lib', 'components', 'prisma', 'types', 'docs']
const EXCLUDE_SEGMENTS = new Set(['node_modules', '.git', '.next'])
const EXCLUDE_PREFIXES = ['docs/_generated']

function shouldExclude(relPath) {
  if (!relPath) return false
  if (EXCLUDE_PREFIXES.some((prefix) => relPath.startsWith(prefix))) return true
  const parts = relPath.split(path.sep)
  return parts.some((part) => EXCLUDE_SEGMENTS.has(part))
}

function walkTree(absDir, relDir = '') {
  const entries = fs.readdirSync(absDir, { withFileTypes: true })
    .sort((a, b) => a.name.localeCompare(b.name))

  const lines = []
  for (const entry of entries) {
    const childRel = relDir ? path.join(relDir, entry.name) : entry.name
    if (shouldExclude(childRel)) continue

    if (entry.isDirectory()) {
      lines.push(`${childRel}/`)
      lines.push(...walkTree(path.join(absDir, entry.name), childRel))
    } else {
      lines.push(childRel)
    }
  }
  return lines
}

function buildContent() {
  const lineItems = []
  for (const root of INCLUDE_ROOTS) {
    const abs = path.join(ROOT, root)
    if (!fs.existsSync(abs)) continue
    lineItems.push(`${root}/`)
    lineItems.push(...walkTree(abs, root))
  }

  const body = lineItems.map((line) => `- \`${line}\``).join('\n')
  return [
    '# File Map',
    '',
    'Generated by `scripts/docs/generate-file-map.mjs`.',
    '',
    body,
    '',
  ].join('\n')
}

function run() {
  const checkMode = process.argv.includes('--check')
  const content = buildContent()

  if (checkMode) {
    if (!fs.existsSync(TARGET)) {
      console.error(`Missing generated file: ${path.relative(ROOT, TARGET)}`)
      process.exit(1)
    }
    const existing = fs.readFileSync(TARGET, 'utf8')
    if (existing !== content) {
      console.error(`Out-of-date file map: ${path.relative(ROOT, TARGET)}. Run: npm run docs:generate`)
      process.exit(1)
    }
    return
  }

  fs.mkdirSync(path.dirname(TARGET), { recursive: true })
  fs.writeFileSync(TARGET, content)
  console.log(`Wrote ${path.relative(ROOT, TARGET)}`)
}

run()

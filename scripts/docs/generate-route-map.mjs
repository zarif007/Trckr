#!/usr/bin/env node
import fs from 'node:fs'
import path from 'node:path'

const ROOT = process.cwd()
const APP_DIR = path.join(ROOT, 'app')
const ROUTE_TARGET = path.join(ROOT, 'docs', '_generated', 'route-map.md')
const MODULE_TARGET = path.join(ROOT, 'docs', '_generated', 'module-map.md')

const ROUTE_FILE_NAMES = new Set(['page.tsx', 'layout.tsx', 'route.ts', 'loading.tsx', 'error.tsx'])

function routePathFromAppFile(relPath) {
  const normalized = relPath.replace(/\\/g, '/')
  const parts = normalized.split('/')
  const fileName = parts.pop()
  const routeSegments = parts.slice(1) // remove "app"

  if (fileName === 'route.ts') {
    return `/${routeSegments.join('/')}`.replace(/\/+/g, '/') || '/'
  }

  if (fileName === 'page.tsx') {
    return `/${routeSegments.join('/')}`.replace(/\/+/g, '/') || '/'
  }

  return `/${routeSegments.join('/')}`.replace(/\/+/g, '/') || '/'
}

function walk(absDir, relDir = 'app') {
  const entries = fs.readdirSync(absDir, { withFileTypes: true })
    .sort((a, b) => a.name.localeCompare(b.name))
  const files = []

  for (const entry of entries) {
    const childAbs = path.join(absDir, entry.name)
    const childRel = path.join(relDir, entry.name)
    if (entry.isDirectory()) {
      files.push(...walk(childAbs, childRel))
      continue
    }
    if (ROUTE_FILE_NAMES.has(entry.name)) {
      files.push(childRel)
    }
  }

  return files
}

function collectModuleRows() {
  const roots = ['app', 'lib', 'components']
  const rows = []

  for (const root of roots) {
    const absRoot = path.join(ROOT, root)
    if (!fs.existsSync(absRoot)) continue

    const stack = [absRoot]
    while (stack.length > 0) {
      const dir = stack.pop()
      const rel = path.relative(ROOT, dir).replace(/\\/g, '/')
      const entries = fs.readdirSync(dir, { withFileTypes: true })
      const files = entries.filter((entry) => entry.isFile())
      const tsCount = files.filter((f) => /\.(ts|tsx)$/.test(f.name)).length
      const readme = files.some((f) => f.name === 'README.md')

      if (tsCount > 0) {
        rows.push({ rel, tsCount, readme })
      }

      for (const entry of entries) {
        if (!entry.isDirectory()) continue
        if (entry.name === 'node_modules' || entry.name === '.next') continue
        stack.push(path.join(dir, entry.name))
      }
    }
  }

  return rows.sort((a, b) => a.rel.localeCompare(b.rel))
}

function buildRouteContent(routeFiles) {
  const rows = routeFiles
    .map((rel) => {
      const file = rel.replace(/\\/g, '/')
      const route = routePathFromAppFile(file)
      const kind = file.endsWith('route.ts') ? 'API handler' : file.endsWith('layout.tsx') ? 'Layout' : file.endsWith('page.tsx') ? 'Page' : file.endsWith('loading.tsx') ? 'Loading' : 'Error'
      return `| \`${route}\` | ${kind} | \`${file}\` |`
    })
    .join('\n')

  return [
    '# Route Map',
    '',
    'Generated by `scripts/docs/generate-route-map.mjs`.',
    '',
    '| Route | Kind | File |',
    '|---|---|---|',
    rows,
    '',
  ].join('\n')
}

function buildModuleContent(rows) {
  const tableRows = rows
    .map((row) => `| \`${row.rel}\` | ${row.tsCount} | ${row.readme ? 'yes' : 'no'} |`)
    .join('\n')

  return [
    '# Module Map',
    '',
    'Generated by `scripts/docs/generate-route-map.mjs`.',
    '',
    '| Module Path | TS/TSX Files | README.md |',
    '|---|---:|---|',
    tableRows,
    '',
  ].join('\n')
}

function compareOrWrite(target, content, checkMode) {
  if (checkMode) {
    if (!fs.existsSync(target)) {
      console.error(`Missing generated file: ${path.relative(ROOT, target)}`)
      process.exit(1)
    }
    const existing = fs.readFileSync(target, 'utf8')
    if (existing !== content) {
      console.error(`Out-of-date generated file: ${path.relative(ROOT, target)}. Run: npm run docs:generate`)
      process.exit(1)
    }
    return
  }

  fs.mkdirSync(path.dirname(target), { recursive: true })
  fs.writeFileSync(target, content)
  console.log(`Wrote ${path.relative(ROOT, target)}`)
}

function run() {
  const checkMode = process.argv.includes('--check')
  const routeFiles = fs.existsSync(APP_DIR) ? walk(APP_DIR) : []
  const routeContent = buildRouteContent(routeFiles)
  const moduleContent = buildModuleContent(collectModuleRows())

  compareOrWrite(ROUTE_TARGET, routeContent, checkMode)
  compareOrWrite(MODULE_TARGET, moduleContent, checkMode)
}

run()

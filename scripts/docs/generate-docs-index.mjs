#!/usr/bin/env node
import fs from 'node:fs'
import path from 'node:path'

const ROOT = process.cwd()
const DOCS_DIR = path.join(ROOT, 'docs')
const TARGET = path.join(DOCS_DIR, '_generated', 'docs-index.md')
const EXCLUDED_SEGMENTS = new Set(['node_modules', '.git', '.next'])

function shouldSkip(relPath) {
  const normalized = relPath.replace(/\\/g, '/')
  const parts = normalized.split('/')
  if (parts.some((part) => EXCLUDED_SEGMENTS.has(part))) return true
  return normalized === 'docs/_generated/docs-index.md'
}

function collectMarkdownFiles(absDir, relDir = 'docs') {
  const out = []
  const entries = fs
    .readdirSync(absDir, { withFileTypes: true })
    .sort((a, b) => a.name.localeCompare(b.name))

  for (const entry of entries) {
    const childAbs = path.join(absDir, entry.name)
    const childRel = path.join(relDir, entry.name)

    if (shouldSkip(childRel)) continue

    if (entry.isDirectory()) {
      out.push(...collectMarkdownFiles(childAbs, childRel))
      continue
    }

    if (entry.isFile() && entry.name.endsWith('.md')) {
      out.push({
        rel: childRel.replace(/\\/g, '/'),
        fromGeneratedRel: path
          .relative(path.dirname(TARGET), childAbs)
          .replace(/\\/g, '/'),
      })
    }
  }

  return out
}

function buildContent() {
  const rows = collectMarkdownFiles(DOCS_DIR)
  const items = rows.map((row) => `- [\`${row.rel}\`](${row.fromGeneratedRel})`).join('\n')

  return [
    '# Docs Index',
    '',
    'Generated by `scripts/docs/generate-docs-index.mjs`.',
    '',
    `Total markdown docs: **${rows.length}**`,
    '',
    items,
    '',
  ].join('\n')
}

function run() {
  const checkMode = process.argv.includes('--check')
  const content = buildContent()

  if (checkMode) {
    if (!fs.existsSync(TARGET)) {
      console.error(`Missing generated file: ${path.relative(ROOT, TARGET)}`)
      process.exit(1)
    }
    const existing = fs.readFileSync(TARGET, 'utf8')
    if (existing !== content) {
      console.error(`Out-of-date docs index: ${path.relative(ROOT, TARGET)}. Run: npm run docs:generate`)
      process.exit(1)
    }
    return
  }

  fs.mkdirSync(path.dirname(TARGET), { recursive: true })
  fs.writeFileSync(TARGET, content)
  console.log(`Wrote ${path.relative(ROOT, TARGET)}`)
}

run()

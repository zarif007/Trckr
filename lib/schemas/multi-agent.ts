import { z } from "zod";
import { gridViewSchema, trackerSchema } from "./tracker";

const suggestedModuleSchema = z
  .object({
    name: z.string().describe('Module name (e.g. "HR", "Finance")'),
    trackerNames: z
      .array(z.string())
      .optional()
      .describe("Tracker names that belong to this module"),
  })
  .passthrough();

export const managerSchema = z
  .object({
    thinking: z
      .string()
      .optional()
      .describe(
        "Detailed thinking process or internal monologue of the manager",
      ),
    prd: z
      .object({
        name: z.string().optional().describe("Short name for the tracker"),
        keyFeatures: z
          .array(z.string())
          .optional()
          .describe("List of key features or goals"),
        suggestedModules: z
          .array(suggestedModuleSchema)
          .optional()
          .describe(
            "Optional modules for multi-domain projects. Omit for simple single-domain projects.",
          ),
      })
      .partial()
      .optional()
      .describe("The Product Requirements Document generated by the manager"),
    builderTodo: z
      .array(
        z
          .object({
            task: z
              .string()
              .optional()
              .describe(
                'The distinct task to perform (e.g., "Change grouping to Priority")',
              ),
            target: z
              .string()
              .optional()
              .describe('The name of the item to change (e.g., "Tasks Grid")'),
            action: z
              .string()
              .optional()
              .describe(
                "The action to take (e.g., create/update/delete/ignore)",
              ),
          })
          .passthrough(),
      )
      .optional()
      .describe(
        "Todo list for the Builder Agent to execute. Derived from comparing the User Request with the Previous Tracker.",
      ),
    requiredMasterData: z
      .array(
        z.object({
          key: z
            .string()
            .describe(
              'Stable snake_case singular key (e.g. "customer", "product")',
            ),
          name: z
            .string()
            .describe(
              'Human-readable Title Case singular name (e.g. "Customer", "Product")',
            ),
          labelFieldId: z
            .string()
            .describe(
              'Field id for select display/value (e.g. "full_name", "product_name")',
            ),
        }),
      )
      .optional()
      .describe(
        "All master data entities this tracker needs for select/multiselect fields (all scopes). Server pre-resolves these for module/project scope before the builder runs.",
      ),
  })
  .passthrough();

export type ManagerSchema = z.infer<typeof managerSchema>;

const patchConfigSchema = z.record(z.string(), z.any()).optional();

const tabPatchSchema = z
  .object({
    id: z.string(),
    name: z.string().optional(),
    placeId: z.coerce.number().optional(),
    config: patchConfigSchema,
    _delete: z.boolean().optional(),
  })
  .passthrough();

const sectionPatchSchema = z
  .object({
    id: z.string(),
    name: z.string().optional(),
    tabId: z.string().optional(),
    placeId: z.coerce.number().optional(),
    config: patchConfigSchema,
    _delete: z.boolean().optional(),
  })
  .passthrough();

const gridPatchSchema = z
  .object({
    id: z.string(),
    name: z.string().optional(),
    sectionId: z.string().optional(),
    placeId: z.coerce.number().optional(),
    config: patchConfigSchema,
    views: z.array(gridViewSchema).optional(),
    _delete: z.boolean().optional(),
  })
  .passthrough();

const fieldPatchSchema = z
  .object({
    id: z.string(),
    dataType: z.string().optional(),
    ui: z
      .object({
        label: z.string().optional(),
        placeholder: z.string().optional(),
      })
      .partial()
      .optional(),
    config: patchConfigSchema,
    _delete: z.boolean().optional(),
  })
  .passthrough();

const renderAsEnum = z
  .enum(["default", "table", "kanban", "calendar", "timeline"])
  .optional();

const layoutNodePatchSchema = z
  .object({
    gridId: z.string(),
    fieldId: z.string(),
    order: z.coerce.number().optional(),
    renderAs: renderAsEnum,
    _delete: z.boolean().optional(),
  })
  .passthrough();

export const trackerPatchSchema = z
  .object({
    name: z.string().optional(),
    tabs: z.array(tabPatchSchema).optional(),
    sections: z.array(sectionPatchSchema).optional(),
    grids: z.array(gridPatchSchema).optional(),
    fields: z.array(fieldPatchSchema).optional(),
    layoutNodes: z.array(layoutNodePatchSchema).optional(),
    bindings: z.record(z.string(), z.any()).optional(),
    bindingsRemove: z.array(z.string()).optional(),
    validations: z.record(z.string(), z.any()).optional(),
    validationsRemove: z.array(z.string()).optional(),
    calculations: z.record(z.string(), z.any()).optional(),
    calculationsRemove: z.array(z.string()).optional(),
    /** Patch dynamic options function/connector definitions. */
    dynamicOptions: z.record(z.string(), z.any()).optional(),
  })
  .passthrough();

const masterDataTrackerSpecSchema = z
  .object({
    key: z
      .string()
      .describe('Stable key for bindings (e.g. "student", "supplier")'),
    name: z.string().describe('Master data tracker name (e.g. "Student")'),
    labelFieldId: z.string().describe("Field id used for select display/value"),
    schema: trackerSchema.describe(
      "Full tracker schema for this master data tracker",
    ),
  })
  .passthrough();

export const multiAgentSchema = z
  .object({
    manager: managerSchema.optional(),
    tracker: trackerSchema.optional(),
    trackerPatch: trackerPatchSchema.optional(),
    masterDataTrackers: z.array(masterDataTrackerSpecSchema).optional(),
  })
  .passthrough();

export type MultiAgentSchema = z.infer<typeof multiAgentSchema>;
export type TrackerPatchSchema = z.infer<typeof trackerPatchSchema>;
export type MasterDataTrackerSpec = z.infer<typeof masterDataTrackerSpecSchema>;
